export const convertToEmojis = (text: string, seed: number | number[]) => {
  let emojis = "";
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    const key = char === "$" ? "dollar" : char.toLowerCase();
    const indexSeed = Array.isArray(seed) ? seed[i % seed.length] : seed;
    const index = Math.floor(indexSeed * charToEmojiMap[key]?.length);
    emojis += charToEmojiMap[key]?.[index] ?? char;
  }

  return emojis;
};
